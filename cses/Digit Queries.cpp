#include <cstdlib>


void run_case() {
    int64_t N;
    cin >> N;

    int64_t base = 9, digits = 1;

    while (N - base * digits > 0) {
        N -= base * digits;
        digits++;
        base *= 10;
    }

    auto [num, index] = div(N, digits);
    if (index != 0)
        num++;

    index = index == 0 ? digits : index;
    cout << to_string(powl(10, digits - 1) + num - 1)[index - 1] << '\n';
}

int main()
{

    int tests;
    cin >> tests;
    while (tests--)
        run_case();

    return 0;
}